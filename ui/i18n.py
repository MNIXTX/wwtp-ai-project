def get_lang() -> str:
    """获取当前语言"""
    if "lang" not in st.session_state:
        st.session_state.lang = "zh"
    return st.session_state.lang


def set_lang(lang: str):
    """切换语言"""
    st.session_state.lang = lang


def t(key: str, *args) -> str:
    """
    获取翻译文本。
    用法: t("app_title") -> "智慧水务 AI 决策平台"
          t("tasks_stopped", 5) -> "已终止 5 个后台训练进程"
    """
    lang = get_lang()
    entry = TRANSLATIONS.get(key)
    if entry is None:
        return f"[MISSING:{key}]"
    text = entry.get(lang, entry.get("zh", key))
    if args:
        text = text.format(*args)
    return text


def lang_selector():
    """渲染语言切换器（放在 sidebar 中）"""
    st.divider()
    st.subheader(t("select_language"))
    current = get_lang()
    cols = st.columns(2)
    with cols[0]:
        if st.button("中文", type="primary" if current == "zh" else "secondary",
                     use_container_width=True, key="lang_zh"):
            set_lang("zh")
            st.rerun()
    with cols[1]:
        if st.button("English", type="primary" if current == "en" else "secondary",
                     use_container_width=True, key="lang_en"):
            set_lang("en")
            st.rerun()
